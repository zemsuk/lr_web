    <div class="row">
        <div class="content">
            <h4><span>1</span>Permanent Prayer Room</h4>
            <p>
TICC has been borne out of the necessity to have permanent Prayer Room in mid- Tipperary where an ever-expanding Islamic Community within Thurles and surrounding areas can attend to practice their faith. It is our priority to ensure the availability of a Prayer Room for our members at all times and to ensure that that Prayer Room is maintained to the highest standard for the safety and comfort of all of our members. Prior to the establishment of TICC, members of the Islamic Community from Thurles, Templemore, Borrisoleigh etc. who wished to participate in prayer services had to travel 40+km to our nearest Islamic centre in Clonmel. </p>
        </div>
        <div class="content">
            <h4><span>2</span>Sunday School</h4>
            <p>
Integral to our objectives as an Islamic Community Centre is the promotion and provision of religious education to the Muslim community of Thurles and surrounding areas. This includes providing religious education for junior members of our community. As of late 2018 we have provided a Sunday school and it is our aim that our Sunday school will continue and expand through participation. </p>
        </div>
        <div class="content">
            <h4><span>3</span>Collaboration with the Local Community Organisations </h4>
            <p>
By 2022 TICC will have established strong links with other religious, charitable and community-based organisations in Thurles and surrounding areas. This will provide TICC the opportunity to engage with local community groups and support them in fundraising efforts for disadvantaged members of our locality. We in turn wish to be in a position to provide same to any TICC members who may require assistance.</p>
        </div>
        <div class="content">
            <h4><span>4</span>Permanent Muslim Community Centre</h4>
            <p>
TICC is an inclusive organisation which welcomes and encourages the participation of new members who wish to partake in the observance of the Islamic religion and its traditions. To correlate with this, we aim to provide a permanent Community Centre for our members, existing and new, so that they may join together for various social functions and gatherings.</p>
        </div>
        <div class="content">
            <h4><span>5</span>International Relief Efforts</h4>
            <p>
Our members come from many different countries, many of which face natural or humanitarian crises from time to time. Similar to our objective of providing support to disadvantaged persons in the locality of Thurles and surrounding areas, we will also endeavour to fundraise, and provide support and assistance, where feasible, to international communities where natural or humanitarian disasters have occurred.</p>
        </div>
        <div class="content">
            <h4><span>6</span>Funeral & Repatriation Support</h4>
            <p>Our members come from diverse backgrounds and many different countries. Each member of the community has a common duty to support both the community as a whole and one another. In the event of the death of one of our members we will endeavour to provide assistance to relatives and friends of the deceased. We aim to have provisions in place to help with funeral expenses and repatriation costs for any of our members who may require such financial assistance.</p>
